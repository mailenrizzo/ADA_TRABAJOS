let texto = "Hola mundo";   //tipo string o cadena de texto
let booleano = true;    //tipo boolean (verdadero/true o falso/false)
let indefinido;       //variable no inicianizada (undefined)
let nulo = null       //valos nulo (vacio)
let objeto = { nombre: "luis", edad:30 };    // objeto con diferentes propiedades
let array = ["frutilla", "manzana", "naranja"];    //array con dif valores


// Mostramos los valores por consola y explicamos su tipo:
console.log("numero:", numero);
console.log("texto:", texto);
console.log("booleano:", booleano);
console.log("indefinido:", indefinido);
console.log("nulo:", nulo);
